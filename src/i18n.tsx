export type TranslationKeys = keyof typeof translations["ko"];

interface LanguageContextProps {
  currentLanguage: Language;
  setLanguage: (lang: Language) => void;
  setCurrentLanguage: (lang: Language) => void;
  t: (key: TranslationKeys) => string;
}

const LanguageContext = createContext<LanguageContextProps | undefined>(undefined);

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [currentLanguage, setCurrentLanguageState] = useState<Language>("ko");

  useEffect(() => {
    const savedLanguage = localStorage.getItem("jeopsi_lang") as Language;
    if (savedLanguage && (savedLanguage === "ko" || savedLanguage === "en" || savedLanguage === "de")) {
      setCurrentLanguageState(savedLanguage);
    }
  }, []);

  const setLanguage = (lang: Language) => {
    setCurrentLanguageState(lang);
    localStorage.setItem("jeopsi_lang", lang);
  };

  const t = (key: TranslationKeys): string => {
    const dic = translations[currentLanguage] || translations["ko"];
    return dic[key] || translations["ko"][key] || String(key);
  };

  return (
    <LanguageContext.Provider value={{ currentLanguage, setLanguage, setCurrentLanguage: setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error("useLanguage must be used within a LanguageProvider");
  }
  return context;
}
